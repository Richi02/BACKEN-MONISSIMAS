    public function create(){
        $request = \Config\Services::request();
        $validation = \Config\Services::validation();
        $headers = $request->getHeaders();
        $model = new RegistrosModel();
        $registro = $model->where('estado', 1)->findAll();
        // var_dump($registro); die; 
        foreach($registro as $key=>$value){
            if(array_key_exists('Authorization',$headers) && !empty($headers['Authorization'])){
                if($request->getHeader('Authorization')=='Authorization: Basic '.base64_encode($value['cliente_id'].':'.$value['llave_secreta'])){
                        $datos = array(
                            "usuario"=>$request->getVar("usuario"),
                            "password"=>$request->getVar("password"),
                            "rolid"=>$request->getVar("rolid"),
                            "empresaid"=>$request->getVar("empresaid"),
                            "sucursalid"=>$request->getVar("sucursalid")
                        );
                        if(!empty($datos)){
                            $validation->setRules([
                                "usuario"=>'required|string|max_length[255]',
                                "password"=>'required|string|max_length[255]',
                                "rolid"=>'required|integer',
                                "empresaid"=>'required|integer',
                                "sucursalid"=>'required|integer'
                            ]);
                            $validation->withRequest($this->request)->run();
                            if($validation->getErrors()){
                                $errors = $validation->getErrors();
                                $data = array("Status"=>404, "Detalle"=>$errors);
                                return json_encode($data, true);
                            }
                            else{
                                $datos = array(
                                    "usuario"=>$datos["usuario"],
                                    "password"=>$datos["password"],
                                    "rolid"=>$datos["rolid"],
                                    "empresaid"=>$datos["empresaid"],
                                    "sucursalid"=>$datos["sucursalid"]
                                );
                                $model = new UsersModel();
                                $user = $model->insert($datos);
                                $data = array(
                                    "Status"=>200,
                                    "Detalle"=>"Registro existoso"
                                );
                                return json_encode($data, true);
                            }
                        }
                        else{
                            $data = array(
                                "Status"=>404,
                                "Detalle"=>"Registro con errores"
                            );
                            return json_encode($data, true);
                        }
                }
                else{
                    $data = array(
                        "Status"=>404,
                        "Detalles"=>"El token es incorrecto"
                    );
                }
            }
            else{
                $data = array(
                    "Status"=>404,
                    "Detalles"=>"No posee autorización"
                );
            }
        }
        return json_encode($data, true);
    }
