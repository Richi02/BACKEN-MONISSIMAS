        $model = new RegistrosModel();
        $registro = $model->where('estado', 1)->findAll();
        // var_dump($registro); die; 
        foreach($registro as $key=>$value){
            if(array_key_exists('Authorization',$headers) && !empty($headers['Authorization'])){
                if($request->getHeader('Authorization')=='Authorization: Basic '.base64_encode($value['cliente_id'].':'.$value['llave_secreta'])){
                        $datos = array(
                            "ID_USUARIO" => $request -> getVar("ID_USUARIO"),
                            "aptDate" => $request -> getVar("aptDate"),
                            "aptTime" => $request -> getVar("aptTime"),
                            "ID_SERVICIO" => $request -> getVar("ID_SERVICIO"),
                            "observacion" => $request -> getVar("observacion")
                        );
                        if(!empty($datos)){
                            $validation->setRules([
                                "ID_USUARIO" => 'required|integer',
                                "aptDate" => 'required|string|max_length[255]',
                                "aptTime" => 'required|string|max_length[255]',
                                "ID_SERVICIO" => 'required|integer',
                                "observacion" => 'required|string|max_length[255]' 
                            ]);
                            $validation->withRequest($this->request)->run();
                            if($validation->getErrors()){
                                $errors = $validation->getErrors();
                                $data = array("Status"=>404, "Detalle"=>$errors);
                                return json_encode($data, true);
                            }
                            else{
                                $datos = array(
                                    "ID_USUARIO" => $datos["ID_USUARIO"],
                                    "aptDate" => $datos["aptDate"],
                                    "aptTime" => $datos["aptTime"],
                                    "ID_SERVICIO" => $datos["ID_SERVICIO"],
                                    "telefono" => $datos["telefono"],
                                    "observacion" => $datos["observacion"]
                                );
                                $model = new CitasModel();
                                $cita = $model->insert($datos);
                                $data = array(
                                    "Status"=>200,
                                    "Detalle"=>"Registro existoso"
                                );
                                return json_encode($data, true);
                            }
                        }
                        else{
                            $data = array(
                                "Status"=>404,
                                "Detalle"=>"Registro con errores"
                            );
                            return json_encode($data, true);
                        }
                }
                else{
                    $data = array(
                        "Status"=>404,
                        "Detalles"=>"El token es incorrecto"
                    );
                }
            }
            else{
                $data = array(
                    "Status"=>404,
                    "Detalles"=>"No posee autorización"
                );
            }
        }
        return json_encode($data, true);
    }
